import fs from "node:fs";
import path from "node:path";

const ROOT = process.cwd();

function getArg(flag, fallback = "") {
  const index = process.argv.indexOf(flag);
  if (index === -1) return fallback;
  return process.argv[index + 1] || fallback;
}

const kind = getArg("--kind", "web_platform");
const name = getArg("--name", "new-creation");
const slug = name
  .toLowerCase()
  .replace(/[^a-z0-9]+/g, "-")
  .replace(/^-+|-+$/g, "") || "new-creation";

const incubatorDir = path.join(ROOT, ".agent/workspace/incubator", slug);
fs.mkdirSync(incubatorDir, { recursive: true });

const spec = `# ${name}\n\n## Metadata\n\n- Kind: ${kind}\n- Slug: ${slug}\n- Created by: Aurora Core\n\n## Intent\n\n- Problem:\n- User:\n- Outcome:\n- Constraints:\n\n## Product\n\n- Core flow:\n- Modules:\n- Monetization:\n- Risks:\n\n## Architecture\n\n- Frontend:\n- Backend:\n- Data:\n- Integrations:\n\n## Validation\n\n- Acceptance:\n- Fallback:\n- Next step:\n`;

const tasks = `# ${name} Tasks\n\n| TASK-ID | Status | Owner | Scope | Goal |\n|---|---|---|---|---|\n| ${slug.toUpperCase().replace(/-/g, "_")}-001 | todo | unassigned | ${kind} | Convert initial incubator spec into executable product spec |\n| ${slug.toUpperCase().replace(/-/g, "_")}-002 | todo | unassigned | ${kind} | Define architecture and data model |\n| ${slug.toUpperCase().replace(/-/g, "_")}-003 | todo | unassigned | ${kind} | Define monetization and validation |\n`;

const notes = `# ${name} Notes\n\n## Why this exists\n\nIncubator scaffold generated by Aurora Core Local Runtime.\n\n## Rules\n\n- keep this isolated from the current product until approved\n- convert assumptions into explicit specs\n- do not skip validation and fallback definition\n`;

fs.writeFileSync(path.join(incubatorDir, "SPEC.md"), spec, "utf8");
fs.writeFileSync(path.join(incubatorDir, "TASKS.md"), tasks, "utf8");
fs.writeFileSync(path.join(incubatorDir, "NOTES.md"), notes, "utf8");

console.log(`Aurora bootstrap created at .agent/workspace/incubator/${slug}`);
console.log(`Kind: ${kind}`);
console.log(`Name: ${name}`);
