#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";
import crypto from "node:crypto";

const ROOT = process.cwd();
const HEURISTICS_PATH = path.join(ROOT, ".agent/brain/db/heuristics.jsonl");
const PATTERNS_PATH = path.join(ROOT, ".agent/brain/db/patterns.jsonl");
const ANTI_PATTERNS_PATH = path.join(ROOT, ".agent/brain/db/anti_patterns.jsonl");
const ACTIVITY_PATH = path.join(ROOT, ".agent/brain/db/activity_log.jsonl");

function readJsonl(filePath) {
  if (!fs.existsSync(filePath)) return [];
  return fs
    .readFileSync(filePath, "utf8")
    .split(/\r?\n/)
    .filter(Boolean)
    .map((line) => {
      try { return JSON.parse(line); } catch { return null; }
    })
    .filter(Boolean);
}

function writeJsonl(filePath, entries) {
  const content = entries.map((e) => JSON.stringify(e)).join("\n") + "\n";
  fs.writeFileSync(filePath, content);
}

function analyzePatterns(entries) {
  const patterns = {
    handler: [],
    callback: [],
    memo: [],
    rateLimit: [],
    test: [],
    mock: [],
    validation: [],
    other: [],
  };

  const keywords = {
    handler: ["handler", "async", "ctx"],
    callback: ["callback", "useCallback", "onClick"],
    memo: ["memo", "React.memo", "useMemo"],
    rateLimit: ["rateLimit", "checkRateLimit", "limit"],
    test: ["test", "it(", "expect", "describe"],
    mock: ["mock", "vi.fn()", "spyOn"],
    validation: ["validate", "check", "verify"],
  };

  for (const entry of entries) {
    const content = (entry.content || entry.statement || entry.title || "").toLowerCase();
    let matched = false;

    for (const [category, kws] of Object.entries(keywords)) {
      if (kws.some(kw => content.includes(kw))) {
        patterns[category].push(entry);
        matched = true;
        break;
      }
    }

    if (!matched) {
      patterns.other.push(entry);
    }
  }

  return patterns;
}

