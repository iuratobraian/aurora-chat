import { api } from "../../convex/_generated/api";
import { getConvexClient } from "../../lib/convex/client";
import { NewsArticle, NewsSource, NewsCategory, NewsFilter } from '../types/news';
import logger from '../utils/logger';

const convex = getConvexClient();

export const NEWS_DEGRADATION_MESSAGE = 'No hay noticias disponibles en este momento. Configure fuentes de noticias en el panel de administración.';

export interface MarketNews {
  _id: string;
  _creationTime: number;
  title: string;
  summary: string;
  content: string;
  source: string;
  sourceUrl: string;
  sourceLogo?: string;
  category: 'forex' | 'crypto' | 'commodities' | 'indices' | 'stocks' | 'general';
  sentiment: 'bullish' | 'bearish' | 'neutral';
  relatedPairs: string[];
  relatedAssets: string[];
  imageUrl?: string;
  author?: {
    id: string;
    name: string;
    avatar: string;
    isVerified: boolean;
  };
  isAIGenerated: boolean;
  publishedAt: number;
  views: number;
  likes: string[];
  tags: string[];
  createdAt: number;
}

